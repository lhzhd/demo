def preprocess_text_with_jieba(text):
    # HMM=False 强制匹配词典
    words = jieba.lcut(text, HMM=False)

    # 过滤掉纯空格，只保留有内容的词
    words = [w for w in words if w.strip()]

    return " ".join(words)


class MyDataset(Dataset):
    def __init__(self, split_name, tokenizer=None, max_length=128, data_root=r"E:\taiyang-bert\data"):
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.samples = []
        if self.tokenizer is not None:
            # 这一步非常关键：告诉 BERT 这些是一个整体，不要拆开
            self.tokenizer.add_tokens(tcm_keywords)
            print(f"✅ 已将 {len(tcm_keywords)} 个词强制加入 BERT 词表")

        # --- 1. 构建路径 ---
        split_dir = os.path.join(data_root, split_name)
        input_dir = os.path.join(split_dir, "input")
        label_dir = os.path.join(split_dir, "label")

        if not os.path.exists(input_dir):
            raise FileNotFoundError(f"❌ 错误：找不到输入文件夹 {input_dir}")
        if not os.path.exists(label_dir):
            raise FileNotFoundError(f"❌ 错误：找不到标签文件夹 {label_dir}")

        print(f"📂 正在加载 {split_name} 数据集...")
        all_files = os.listdir(input_dir)
        txt_files = [f for f in all_files if f.endswith('.txt') or '.' not in f]

        if not txt_files:
            raise FileNotFoundError(f"在 {input_dir} 中未找到任何文件")

        count_real = 0
        count_void = 0

        for filename in txt_files:
            text_path = os.path.join(input_dir, filename)
            label_path = os.path.join(label_dir, filename)

            # --- 2. 读取文本内容 ---
            content = None
            try:
                with open(text_path, 'r', encoding='utf-8') as f:
                    content = f.read().strip()
            except UnicodeDecodeError:
                try:
                    with open(text_path, 'r', encoding='gbk') as f:
                        content = f.read().strip()
                except Exception:
                    pass  # 读取失败跳过

            if not content:
                continue

            # --- 3. 读取标签内容 ---
            label_str = None
            if os.path.exists(label_path):
                try:
                    with open(label_path, 'r', encoding='utf-8') as f:
                        label_str = f.read().strip()
                except UnicodeDecodeError:
                    with open(label_path, 'r', encoding='gbk') as f:
                        label_str = f.read().strip()

            if not label_str:
                continue

            # --- 4. 标签映射逻辑 ---
            if "表实" in label_str:
                label = 1
                count_real += 1
            elif "表虚" in label_str:
                label = 0
                count_void += 1
            else:
                try:
                    label = int(label_str)
                    if label == 1:
                        count_real += 1
                    else:
                        count_void += 1
                except ValueError:
                    label = 0  # 默认

            self.samples.append((content, label))

        if len(self.samples) == 0:
            raise ValueError(f"❌ 严重错误：{split_name} 数据集加载后样本数为 0！")

        print(f"✅ {split_name.upper()} 加载完成！样本数: {len(self.samples)} (表实:{count_real}, 表虚:{count_void})")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, item):
        text, label = self.samples[item]

        if self.tokenizer is None:
            return text, label

        # 🔥 核心修改点
        # 1. 先分词，变成 "太阳病 桂枝汤 ..." 这种带空格的字符串
        text_processed = preprocess_text_with_jieba(text)

        # 2. 传入 tokenizer，并设置 is_split_into_words=True
        # 这告诉 BERT：输入已经是分好词的（用空格隔开），请尊重这些边界，不要拆字！
        encoding = self.tokenizer(
            text_processed,
            max_length=self.max_length,
            padding='max_length',
            truncation=True,
            return_tensors='pt',
            is_split_into_words=True
        )

        return (
            encoding['input_ids'].squeeze(0),
            encoding['attention_mask'].squeeze(0),
            encoding.get('token_type_ids', torch.zeros_like(encoding['input_ids'])).squeeze(0),
            torch.tensor(label, dtype=torch.long)
        )


